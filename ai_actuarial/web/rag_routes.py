from flask import Flask, jsonify, request

logger = logging.getLogger(__name__)


def register_rag_routes(
    app: Flask,
    db_path: str,
    require_permissions,
    *,
    start_background_task: Callable[..., str] | None = None,
    get_kb_tasks: Callable[[str, int], dict[str, list[dict[str, Any]]]] | None = None,
) -> None:
    from ai_actuarial.storage import Storage

    try:
        from ai_actuarial.rag.knowledge_base import KnowledgeBaseManager
    except ImportError:
        rag_available = False
    else:
        rag_available = True

    def _api_error(message: str, *, status_code: int, detail: str | None = None):
        payload: dict[str, Any] = {"success": False, "error": message}
        if detail and os.getenv("EXPOSE_ERROR_DETAILS"):
            payload["detail"] = detail
        return jsonify(payload), status_code

    def _api_success(data: Any = None, message: str | None = None, status_code: int = 200):
        payload: dict[str, Any] = {"success": True}
        if data is not None:
            payload["data"] = data
        if message:
            payload["message"] = message
        return jsonify(payload), status_code

    def _check_rag_available():
        if not rag_available:
            return _api_error("RAG functionality not available", status_code=503)
        return None

    def _check_config_write_auth():
        expected_token = app.config.get("CONFIG_WRITE_AUTH_TOKEN") or os.getenv(
            "CONFIG_WRITE_AUTH_TOKEN"
        )
        if expected_token:
            provided_token = request.headers.get("X-Auth-Token")
            if not provided_token or provided_token != expected_token:
                logger.warning("RAG write operation rejected: authentication failed")
                return _api_error("Forbidden", status_code=403)
        return None

    def _norm(s: Any) -> str:
        return str(s or "").strip()

    def _list(v: Any, field: str) -> list[str]:
        if v is None:
            return []
        if not isinstance(v, list):
            raise ValueError(f"{field} must be a list")
        out: list[str] = []
        for item in v:
            val = _norm(item)
            if val and val not in out:
                out.append(val)
        return out

    def _kb_id(kb_id: Any) -> str:
        val = _norm(kb_id)
        if not val:
            raise ValueError("kb_id is required")
        # Keep a reasonable length constraint, but do not restrict the character set
        # so that KB IDs generated by KnowledgeBaseManager (e.g. from categories)
        # remain accessible via this API.
        if not (2 <= len(val) <= 64):
            raise ValueError("kb_id must be between 2 and 64 characters long")
        return val

    def _serialize_kb(kb) -> dict[str, Any]:
        return {
            "kb_id": kb.kb_id,
            "name": kb.name,
            "description": kb.description,
            "kb_mode": kb.kb_mode,
            "embedding_model": kb.embedding_model,
            "chunk_size": kb.chunk_size,
            "chunk_overlap": kb.chunk_overlap,
            "index_type": kb.index_type,
            "file_count": kb.file_count,
            "chunk_count": kb.chunk_count,
            "created_at": kb.created_at,
            "updated_at": kb.updated_at,
        }

    def _build_category_where(categories: list[str]) -> tuple[str, list[str]]:
        if not categories:
            return "1=0", []
        parts: list[str] = []
        params: list[str] = []
        for category in categories:
            parts.append("(c.category = ? OR c.category LIKE ? OR c.category LIKE ? OR c.category LIKE ?)")
            params.extend([category, f"{category};%", f"%; {category}", f"%; {category};%"])
        return " OR ".join(parts), params

    def _with_manager(fn):
        storage = Storage(db_path)
        try:
            return fn(KnowledgeBaseManager(storage), storage)
        finally:
            storage.close()

    @app.route("/api/rag/knowledge-bases", methods=["GET"])
    @require_permissions("catalog.read")
    def api_rag_list_kbs():
        if (r := _check_rag_available()) is not None:
            return r
        try:
            kb_mode = _norm(request.args.get("kb_mode"))
            search = _norm(request.args.get("search")).lower()

            def _run(kb_manager, _storage):
                kbs = kb_manager.list_kbs()
                if kb_mode:
                    kbs = [kb for kb in kbs if kb.kb_mode == kb_mode]
                if search:
                    kbs = [
                        kb
                        for kb in kbs
                        if search in (kb.name or "").lower()
                        or search in (kb.description or "").lower()
                        or search in (kb.kb_id or "").lower()
                    ]
                return _api_success([_serialize_kb(kb) for kb in kbs])

            return _with_manager(_run)
        except Exception as exc:  # noqa: BLE001
            logger.exception("Error listing KBs")
            return _api_error("Internal server error", status_code=500, detail=str(exc))

    @app.route("/api/rag/knowledge-bases", methods=["POST"])
    @require_permissions("config.write")
    def api_rag_create_kb():
        if (r := _check_rag_available()) is not None:
            return r
        if (r := _check_config_write_auth()) is not None:
            return r
        try:
            data = request.get_json(silent=True)
            if not isinstance(data, dict):
                return _api_error("Invalid JSON body", status_code=400)
            kb_id = _kb_id(data.get("kb_id"))
            name = _norm(data.get("name"))
            if not name:
                return _api_error("name is required", status_code=400)
            kb_mode = _norm(data.get("kb_mode") or "manual").lower()
            if kb_mode not in {"manual", "category"}:
                return _api_error("kb_mode must be 'category' or 'manual'", status_code=400)
            chunk_size = int(data.get("chunk_size") or 800)
            chunk_overlap = int(data.get("chunk_overlap") or 100)
            categories = _list(data.get("categories"), "categories")
            file_urls = _list(data.get("file_urls"), "file_urls")
            if kb_mode == "category" and not categories:
                return _api_error("categories required for category mode", status_code=400)

            def _run(kb_manager, _storage):
                if kb_manager.get_kb(kb_id):
                    return _api_error(f"Knowledge base '{kb_id}' already exists", status_code=409)
                kb_manager.create_kb(
                    kb_id=kb_id,
                    name=name,
                    description=_norm(data.get("description")),
                    kb_mode=kb_mode,
                    embedding_model=_norm(data.get("embedding_model") or "text-embedding-3-large"),
                    chunk_size=chunk_size,
                    chunk_overlap=chunk_overlap,
                )
                if kb_mode == "category":
                    kb_manager.link_kb_to_categories(kb_id, categories)
                elif file_urls:
                    kb_manager.add_files_to_kb(kb_id, file_urls)
                kb = kb_manager.get_kb(kb_id)
                return _api_success(
                    _serialize_kb(kb),
                    message=f"Knowledge base '{name}' created successfully",
                    status_code=201,
                )

            return _with_manager(_run)
        except ValueError as exc:
            return _api_error(str(exc), status_code=400)
        except Exception as exc:  # noqa: BLE001
            logger.exception("Error creating KB")
            return _api_error("Internal server error", status_code=500, detail=str(exc))

