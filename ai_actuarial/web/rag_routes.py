    @app.route("/api/rag/knowledge-bases/<kb_id>/index", methods=["POST"])
    @require_permissions("tasks.run")
    def api_rag_create_index_task(kb_id: str):
        if (r := _check_rag_available()) is not None:
            return r
        if (r := _check_config_write_auth()) is not None:
            return r
        try:
            kid = _kb_id(kb_id)
            data = request.get_json(silent=True) or {}
            if not isinstance(data, dict):
                return _api_error("Invalid JSON body", status_code=400)
            force_reindex = bool(data.get("force_reindex", False) or data.get("reindex_all", False))
            incremental = bool(data.get("incremental", True))
            requested = _list(data.get("file_urls"), "file_urls")

            def _run(kb_manager, _storage):
                kb = kb_manager.get_kb(kid)
                if not kb:
                    return _api_error(f"Knowledge base '{kid}' not found", status_code=404)
                user_requested = bool(requested)
                files_to_index = requested
                if not files_to_index:
                    if force_reindex or not incremental:
                        files_to_index = [row.get("file_url") for row in kb_manager.get_kb_files(kid)]
                        files_to_index = [url for url in files_to_index if url]
                    else:
                        files_to_index = kb_manager.get_files_needing_index(kid)
                if not files_to_index:
                    return _api_error("No files to index", status_code=400)

                skipped_no_markdown = 0
                if not user_requested:
                    # Auto mode: index only files with markdown content.
                    # Use batching to avoid SQLite parameter limit (999 by default).
                    markdown_urls = set()
                    batch_size = 900  # Stay well under 999 parameter limit
                    for i in range(0, len(files_to_index), batch_size):
                        batch = files_to_index[i:i + batch_size]
                        placeholders = ",".join(["?" for _ in batch])
                        markdown_rows = _storage._conn.execute(
                            f"""
                                SELECT DISTINCT file_url
                                FROM catalog_items
                                WHERE file_url IN ({placeholders})
                                  AND markdown_content IS NOT NULL
                                  AND markdown_content != ''
                            """,
                            batch,
                        ).fetchall()
                        markdown_urls.update(row[0] for row in markdown_rows if row and row[0])
                    original_count = len(files_to_index)
                    files_to_index = [url for url in files_to_index if url in markdown_urls]
                    skipped_no_markdown = max(0, original_count - len(files_to_index))
                    if not files_to_index:
                        return _api_error(
                            "No markdown files to index (all candidates missing markdown)",
                            status_code=400,
                        )

                task_payload = {
                    "type": "rag_indexing",
                    "kb_id": kid,
                    "file_urls": files_to_index,
                    "force_reindex": force_reindex,
                    "incremental": incremental,
                    "name": f"RAG Indexing: {kb.name}",
                }
                if start_background_task is not None:
                    task_id = start_background_task(
                        "rag_indexing",
                        task_payload,
                        task_name=f"RAG Indexing: {kb.name}",
                        extra_fields={"kb_id": kid, "kb_name": kb.name, "rag_file_count": len(files_to_index)},
                    )
                else:
                    task_id = f"rag_index_{kid}_{int(datetime.now().timestamp())}"
                return _api_success(
                    {
                        "job_id": task_id,
                        "kb_id": kid,
                        "file_count": len(files_to_index),
                        "skipped_no_markdown": skipped_no_markdown,
                        "force_reindex": force_reindex,
                        "incremental": incremental,
                        "status": "pending",
                    },
                    status_code=202,
                )

            return _with_manager(_run)
        except ValueError as exc:
            return _api_error(str(exc), status_code=400)
        except Exception as exc:  # noqa: BLE001
            logger.exception("Error creating RAG index task for %s", kb_id)
            return _api_error("Internal server error", status_code=500, detail=str(exc))

