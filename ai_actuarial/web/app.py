_TASK_LOG_DIR = Path("data") / "task_logs"


def _task_log_path(task_id: str) -> Path:
    # Prevent path traversal: task IDs are generated by the server, but also validate in case.
    safe_id = re.sub(r"[^A-Za-z0-9_.-]+", "_", task_id or "unknown")
    return _TASK_LOG_DIR / f"{safe_id}.log"


def _append_task_log(task_id: str, level: str, message: str) -> None:
    """Append a line to the per-task application log."""
    try:
        _TASK_LOG_DIR.mkdir(parents=True, exist_ok=True)
        stamp = datetime.now().isoformat(timespec="seconds")
        line = f"{stamp} [{level.upper()}] {message}\n"
        _task_log_path(task_id).open("a", encoding="utf-8", errors="replace").write(line)
    except Exception:
        # Never let logging break task execution.
        return


def _tail_text_file(path: Path, max_lines: int = 400) -> str:
    """Read the last N lines of a UTF-8 text file (best-effort)."""
    try:
        if not path.exists():
            return ""
        with path.open("r", encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
        return "".join(lines[-max_lines:])
    except Exception:
        return ""


def _env_flag(name: str, default: bool = False) -> bool:
    raw = os.getenv(name)
    if raw is None:
        return default
    return str(raw).strip().lower() in {"1", "true", "yes", "y", "on"}


def _expose_error_details() -> bool:
    # Never enable by default when exposed to the public Internet.
    return _env_flag("EXPOSE_ERROR_DETAILS", False) or _env_flag("FLASK_DEBUG", False)


def _parse_int_clamped(
    value: object,
    *,
    default: int,
    min_value: int | None = None,
    max_value: int | None = None,
) -> int:
    try:
        n = int(value)  # type: ignore[arg-type]
    except Exception:
        n = default
    if min_value is not None:
        n = max(min_value, n)
    if max_value is not None:
        n = min(max_value, n)
    return n


def _api_error(message: str, *, status_code: int, detail: str | None = None):
    payload: dict[str, Any] = {"error": message}
    if detail and _expose_error_details():
        payload["detail"] = detail
    return jsonify(payload), status_code


