def _get_client_ip() -> str:
    """Return the client IP address from the request."""
    # Trust X-Forwarded-For only when explicitly configured (TRUST_PROXY=true).
    if _env_flag("TRUST_PROXY", False):
        xff = request.headers.get("X-Forwarded-For", "")
        if xff:
            return xff.split(",")[0].strip()
    return request.remote_addr or "unknown"


def _extract_bearer_token() -> str | None:
    auth = request.headers.get("Authorization", "") or ""
    m = re.match(r"^Bearer\s+(.+)$", auth.strip(), flags=re.IGNORECASE)
    if m:
        return m.group(1).strip()
    # Compatibility header (optional)
    x = request.headers.get("X-API-Token")
    if x:
        return x.strip()
    return None


