    @app.route("/api/export")
    @require_permissions("export.read")
    @_limit("10 per hour")
    def export_data():
        """Export catalog data as CSV/JSON."""
        format_type = request.args.get('format', 'csv')
        
        # Connect to DB
        try:
            storage = Storage(db_path)

            # Use Storage abstraction to query files with catalog information
            rows = storage.query_files_with_catalog()

            # Normalize rows to a list of dictionaries
            data = []
            for row in rows:
                if isinstance(row, dict):
                    data.append(dict(row))
                else:
                    # Fallback for row objects with keys() / mapping interface
                    try:
                        data.append({k: row[k] for k in row.keys()})  # type: ignore[attr-defined]
                    except Exception:
                        # As a last resort, attempt to use _mapping (e.g., sqlite Row)
                        mapping = getattr(row, "_mapping", None)
                        if mapping is not None:
                            data.append(dict(mapping))
                        else:
                            # If we cannot interpret the row, skip it
                            continue
            
            # Format keywords
            for d in data:
                # Process keywords
                raw_kws = d.get('keywords_json')
                d['keywords'] = ""
                if raw_kws:
                    try:
                        keywords_list = json.loads(raw_kws)
                        if isinstance(keywords_list, list):
                            d['keywords'] = ", ".join(keywords_list)
                        else:
                            d['keywords'] = str(keywords_list)
                    except Exception as e:
                        logger.warning(
                            "Failed to parse keywords_json for catalog export: %r (%s)",
                            raw_kws,
                            e,
                        )
                
                # Cleanup internal fields
                if 'keywords_json' in d:
                    del d['keywords_json']

            storage.close()

            if format_type == 'json':
                return jsonify(data)
            
            # CSV export
            si = io.StringIO()
            cw = csv.DictWriter(si, fieldnames=data[0].keys() if data else [])
            cw.writeheader()
            cw.writerows(data)
            output = si.getvalue()
            
            # Use Latin-1 or UTF-8-SIG for Excel compatibility
            return Response(
                output.encode('utf-8-sig'),
                mimetype="text/csv",
                headers={"Content-disposition": "attachment; filename=catalog_export.csv"}
            )
            
        except Exception as e:
            logger.exception("Export failed")
            if _expose_error_details():
                return f"Export failed: {e}", 500
            return "Export failed", 500

    def _start_background_task(
        collection_type: str,
        data: dict[str, Any],
        *,
        task_name: str | None = None,
        extra_fields: dict[str, Any] | None = None,
    ) -> str:
        """Create and start a background task, returning task ID."""
        task_id = f"task_{int(time.time() * 1000)}_{secrets.token_hex(2)}"
        name = task_name or data.get("name", f"{collection_type.capitalize()} Collection")
        log_file = str(_task_log_path(task_id))
        task_data: dict[str, Any] = {
            "id": task_id,
            "name": name,
            "type": collection_type,
            "status": "pending",
            "progress": 0,
            "started_at": datetime.now().isoformat(),
            "items_processed": 0,
            "items_total": 0,
            "log_file": log_file,
        }
        if extra_fields:
            task_data.update(extra_fields)

        with _task_lock:
            _active_tasks[task_id] = task_data

        _append_task_log(task_id, "INFO", f"Task created (type={collection_type})")

        thread = threading.Thread(
            target=execute_collection_task,
            args=(task_id, collection_type, data),
            daemon=True,
        )
        thread.start()
        return task_id

    def _get_rag_kb_tasks(kb_id: str, limit: int = 20) -> dict[str, list[dict[str, Any]]]:
        """Get active and history task rows for one KB's indexing jobs."""
        with _task_lock:
            active = [
                dict(task)
                for task in _active_tasks.values()
                if task.get("type") in {"rag_indexing", "kb_index_build"}
                and task.get("kb_id") == kb_id
            ]
            history = [
                dict(task)
                for task in _task_history
                if task.get("type") in {"rag_indexing", "kb_index_build"}
                and task.get("kb_id") == kb_id
            ]
        active.sort(key=lambda t: t.get("started_at", ""), reverse=True)
        history.sort(key=lambda t: t.get("started_at", ""), reverse=True)
        return {"active": active[:limit], "history": history[:limit]}

    @app.route("/api/collections/run", methods=["POST"])
    @require_permissions("tasks.run")
    @_limit("10 per minute")
    def run_collection():
        """Start a collection operation."""
        try:
            data = request.get_json()
            if not data:
                return jsonify({"error": "Invalid JSON"}), 400
            
            collection_type = data.get("type")
            valid_types = [
                "scheduled",
                "adhoc",
                "url",
                "file",
                "search",
                "catalog",
                "quick_check",
                "markdown_conversion",
                "chunk_generation",
                "rag_indexing",
                "kb_index_build",
            ]

            def _reject_request(reason: str, *, override_type: str | None = None):
                """Reject a request and still persist a history record for visibility."""
                req_type = override_type or (collection_type or "unknown")
                task_id = f"rejected_{int(datetime.now().timestamp())}"
                task_name = data.get("name", f"{req_type} (rejected)")
                stamp = datetime.now().isoformat()
                log_file = str(_task_log_path(task_id))
                _append_task_log(task_id, "ERROR", f"Rejected request: {reason}")
                task_data = {
                    "id": task_id,
                    "name": task_name,
                    "type": req_type,
                    "status": "error",
                    "progress": 100,
                    "started_at": stamp,
                    "completed_at": stamp,
                    "current_activity": "Rejected",
                    "items_processed": 0,
                    "items_total": 0,
                    "log_file": log_file,
                    "errors": [reason],
                }
                logger.warning("Rejecting collection request (type=%s): %s", req_type, reason)
                with _task_lock:
                    _task_history.append(task_data)
                    _append_history_to_disk(task_data)
                return jsonify({"error": reason}), 400

            if not collection_type or collection_type not in valid_types:
                return _reject_request("Invalid collection type", override_type=collection_type)

            # Quick validation before starting background task
            if collection_type == "url" and not data.get("urls"):
                 return _reject_request("No URLs provided")
            if collection_type == "file":
                 path = data.get("directory_path")
                 if not path or not os.path.exists(path):
                     return _reject_request("Invalid directory path")
            if collection_type == "catalog":
                 scope_mode = str(data.get("scope_mode") or "index").strip().lower()
                 if scope_mode == "category" and not str(data.get("category") or "").strip():
                     return _reject_request("Category is required for category-scoped cataloging")
            if collection_type == "markdown_conversion":
                 scope_mode = str(data.get("scope_mode") or "index").strip().lower()
                 if scope_mode == "category" and not str(data.get("category") or "").strip():
                     return _reject_request("Category is required for category-scoped markdown conversion")
                 has_urls = bool(data.get("file_urls"))
                 has_scan = data.get("scan_count") not in (None, "", "null")
                 if not has_urls and not has_scan:
                     return _reject_request("No files selected for markdown conversion")
            if collection_type == "chunk_generation":
                 scope_mode = str(data.get("scope_mode") or "index").strip().lower()
                 if scope_mode == "category" and not str(data.get("category") or "").strip():
                     return _reject_request("Category is required for category-scoped chunk generation")
                 has_urls = bool(data.get("file_urls"))
                 has_scan = data.get("scan_count") not in (None, "", "null")
                 if not has_urls and not has_scan:
                     return _reject_request("No files selected for chunk generation")
            if collection_type in {"rag_indexing", "kb_index_build"} and not str(data.get("kb_id") or "").strip():
                 return _reject_request(f"kb_id is required for {collection_type}")

            task_name = data.get("name", f"{collection_type.capitalize()} Collection")
            task_id = _start_background_task(collection_type, data, task_name=task_name)
            
            return jsonify({
                "success": True, 
                "message": "Task started in background",
                "job_id": task_id
            })

        except Exception as e:
            logger.exception("Error starting collection")
            return _api_error("Internal server error", status_code=500, detail=str(e))
    
